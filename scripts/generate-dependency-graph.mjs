#!/usr/bin/env node

import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";
import { execSync } from "node:child_process";

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const root = path.resolve(__dirname, "..");
const outPath = path.join(root, "DOCS", "dependency-graph.md");

function getTrackedMjsFiles() {
  const raw = execSync("git ls-files '*.mjs'", {
    cwd: root,
    encoding: "utf8"
  });

  const all = raw
    .split("\n")
    .map((s) => s.trim())
    .filter(Boolean);

  return all.filter((relPath) => {
    if (relPath.startsWith("codecs/")) return true;
    if (relPath.includes("/")) return false;
    if (relPath.startsWith("test")) return false;
    if (relPath.includes("-test")) return false;
    if (relPath === "inspect-input-type.mjs") return false;
    if (relPath === "examples-migration.mjs") return false;
    return true;
  });
}

function parseLocalImports(absPath) {
  const source = fs.readFileSync(absPath, "utf8");
  const regex = /^\s*import\s+[^;]*?from\s+["']([^"']+)["'];?\s*$/gm;
  const imports = [];
  let match;
  while ((match = regex.exec(source)) !== null) {
    const specifier = match[1];
    if (specifier.startsWith("./") || specifier.startsWith("../")) {
      imports.push(specifier);
    }
  }
  return imports;
}

function toNodeId(relPath) {
  return relPath.replace(/[^a-zA-Z0-9]/g, "_");
}

function makeBar(sizeKb) {
  const units = Math.max(1, Math.min(20, Math.round(sizeKb)));
  return "█".repeat(units);
}

function renderGraph(files, sizesKb, edges) {
  const lines = [];
  lines.push("# Project Dependency Graph");
  lines.push("");
  lines.push("_Auto-generated by scripts/generate-dependency-graph.mjs._");
  lines.push("");
  lines.push("```mermaid");
  lines.push("graph TD");
  lines.push("    classDef default fill:#fff,stroke:#333,stroke-width:2px,color:#000;");
  lines.push("");

  for (const relPath of files) {
    const id = toNodeId(relPath);
    const sizeKb = sizesKb[relPath];
    const bar = makeBar(sizeKb);
    lines.push(`    ${id}[\"${relPath} (${sizeKb.toFixed(1)} KB)<br/>${bar}\"]`);
  }

  if (edges.length > 0) {
    lines.push("");
  }

  for (const { src, dst } of edges) {
    lines.push(`    ${toNodeId(src)} --> ${toNodeId(dst)}`);
  }

  lines.push("```");
  lines.push("");
  return lines.join("\n");
}

function main() {
  const files = getTrackedMjsFiles().sort();
  const fileSet = new Set(files);

  const sizesKb = {};
  for (const relPath of files) {
    const absPath = path.join(root, relPath);
    const bytes = fs.statSync(absPath).size;
    sizesKb[relPath] = bytes / 1024;
  }

  const edgeKeys = new Set();
  const edges = [];
  for (const relPath of files) {
    const absPath = path.join(root, relPath);
    const imports = parseLocalImports(absPath);

    for (const specifier of imports) {
      const resolved = path.normalize(path.join(path.dirname(relPath), specifier));
      if (!resolved.endsWith(".mjs")) continue;
      if (!fileSet.has(resolved)) continue;
      const key = `${relPath}->${resolved}`;
      if (edgeKeys.has(key)) continue;
      edgeKeys.add(key);
      edges.push({ src: relPath, dst: resolved });
    }
  }

  const graph = renderGraph(files, sizesKb, edges);
  fs.writeFileSync(outPath, graph, "utf8");
  console.log(`Wrote ${path.relative(root, outPath)}`);
  console.log(`Nodes: ${files.length}, Edges: ${edges.length}`);
}

main();
